!$OMP DO PRIVATE(sign,ijkQ,iP2,jP2,kP2,iP1,jP1,kP1,tp,up,vp,signijk,ituvP)
  DO i = 1, nPrimP
   sign = signQ 
   ijkQ=0
   DO iP2=l2,0,-1
    DO jP2=l2-iP2,0,-1
     kP2=l2-iP2-jP2
     DO iP1=l1,0,-1
      DO jP1=l1-iP1,0,-1
       kP1=l1-iP1-jP1
