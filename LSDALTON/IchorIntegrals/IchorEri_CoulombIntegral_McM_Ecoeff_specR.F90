               & *ETIJ(i,up,jP1,jP2,2)*ETIJ(i,vp,kP1,kP2,3)*PreExpFac(i)
         ENDDO
        ENDDO
       ENDDO
      ENDDO
     ENDDO
    ENDDO
   ENDDO
  ENDDO
!$OMP END DO
