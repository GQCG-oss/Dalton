  real(realk),intent(inout) :: EcoeffN(nPrimP,nTUV,ijk)
  !
  integer :: l2,l1,iP2,JP2,kp2,jp1,ip1,kp1,tp,up,vp,ituvp,J,ijkQ,i
  integer :: ii,iAtomA,iAtomB
